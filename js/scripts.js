var a = 5;
var b = 3;
var value = (a * a) - (2 * a * b) - (b * b);

console.log('The result is:', value);
if (value > 0) {
    console.log('The result is positive');
}
if (value < 0) {
    console.log('The result is negative');
}
if (value === 0) {
    console.log('The result equals 0');
}